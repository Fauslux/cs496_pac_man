package client;

/**
 * This class is used to hold a long object for the game
 * @author Kevin Richardson
 * @version March 2023
 */

public class LongObject
{
	// The value of the long
    public long value;
    
    /**
     * Constructor for the LongObject
     * @param i The initial long value
     */
    public LongObject(long i)
    {
        value = i;
    }
}